                           Y(1), Y(2), Y(3), Y(4), X(5), ...
                           Y(6), Y(7), Y(8), Y(9), ...
                           interp(1), interp(2), interp(3), interp(4), ...
                           interp(5), interp(6), interp(7), interp(8), ...
                           interp(9));
  end
end

